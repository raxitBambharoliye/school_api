const mongoose=require('mongoose');
const Schema=mongoose.Schema({
    username:{
        type:String,
        required:true
    },
    email:{
        type:String,
        required:true
    },
    password:{
        type:String,
        required:true
    },
    role:{
        type:String,
        required:true
    }
})

const admin=mongoose.model('admin',Schema);

module.exports=admin;